# Моделирование газа с упругими столкновениями. 
# Частицы газа моделируются шариками конечного размера, которые упруго сталкиваются друг с другом. Столкновение моделируется как обмен 
# нормальными составляющими скоростей двух частиц в системе отсчета, связанной с центром масс этих частиц. Дифференциальное уравнение решается
# методом Эйлера.

# --- Гиперпараметры ---
R = 0.2    # радиус частицы
m = 1      # единичная масса частицы

# --- Моделирование ---
# particle = [ [x, y, z], [Vx, Vy, Vz] ]
import numpy as np

# движение частицы
def move(p, dt):
    p[0] += dt * p[1]
    return p

# столкновение двух частиц
def particle_collision(p1, p2):
    # единичный вектор нормали
    r = p1[0] - p2[0]
    n = r / np.linalg.norm(r)
    # проекции скоростей на n
    V1n = p1[1] @ n
    V2n = p2[1] @ n
    # проекции скоростей на тангенс
    V1t = p1[1] - V1n * n
    V2t = p2[1] - V2n * n
    # обмен нормальными составляющими (упругое столкновение)
    V1n, V2n = V2n, V1n
    # конечные скорости
    p1[1] = V1t + V1n * n
    p2[1] = V2t + V2n * n
    # выталкиваем частицы, чтобы они не пересекались
    d = 2 * R - np.linalg.norm(r)
    p1[0] += 0.5 * d * n
    p2[0] -= 0.5 * d * n
    return p1, p2

# Проверка столкновения
def check_collision(p1, p2):
    return np.linalg.norm(p1[0] - p2[0]) < 2 * R

# Моделирование с упругими столкновениями
def modeling(particles, dt):
    new_particles = particles
    # двигаем все частицы
    for i in range(len(new_particles)):
        new_particles[i] = move(new_particles[i], dt)
    # проверяем и обрабатываем столкновения
    for i in range(len(new_particles)):
        for j in range(i+1, len(new_particles)):
            if check_collision(new_particles[i], new_particles[j]):
                new_particles[i], new_particles[j] = particle_collision(new_particles[i], new_particles[j])
    return new_particles

